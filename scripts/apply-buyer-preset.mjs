#!/usr/bin/env node
/**
 * apply-buyer-preset.mjs
 * Non-interactive version of setup-buyer — reads a buyers/<slug>/config.json
 * and writes branch.config.ts + patches app.json directly.
 *
 * Usage:  node scripts/apply-buyer-preset.mjs buyers/xyz-financial/config.json
 */

import fs   from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT      = path.join(__dirname, '..');

const presetArg = process.argv[2];
if (!presetArg) {
  console.error('Usage: node scripts/apply-buyer-preset.mjs <path/to/config.json>');
  process.exit(1);
}

const presetPath = path.resolve(ROOT, presetArg);
if (!fs.existsSync(presetPath)) {
  console.error(`Preset not found: ${presetPath}`);
  process.exit(1);
}

const cfg = JSON.parse(fs.readFileSync(presetPath, 'utf8'));

// ── Write branch.config.ts ────────────────────────────────────────────────────

const branchConfigContent = `// AUTO-GENERATED by scripts/apply-buyer-preset.mjs — ${new Date().toISOString()}
// Buyer: ${cfg.branchName}
// Preset: ${presetArg}
// To regenerate: node scripts/apply-buyer-preset.mjs ${presetArg}

const branchConfig = {
  // Branding
  appName:         '${cfg.appName}',
  logo:             require('./assets/${cfg.logoFile ?? 'icon.png'}'),
  primaryColor:    '${cfg.primaryColor}',
  backgroundColor: '${cfg.backgroundColor}',

  // Branch identity
  branchName: '${cfg.branchName}',
  agentName:  '${cfg.agentName}',
  location:   '${cfg.location}',
  phone:      '${cfg.phone}',
  email:      '${cfg.email}',

  // Scheduling & contact
  calendlyUrl:     '${cfg.calendlyUrl}',
  messengerPageId: '${cfg.messengerPageId}',

  // Integrations
  n8nWebhook:      '${cfg.n8nWebhook}',
  googleSheetsKey: '${cfg.googleSheetsKey}',
  appsScriptUrl:   '${cfg.appsScriptUrl}',

  // WebView URLs
  webviewUrl:    '${cfg.webviewUrl}',
  assessmentUrl: '${cfg.assessmentUrl}',
  resultsUrl:    '${cfg.resultsUrl}',

  // Store / build
  bundleId:         '${cfg.bundleId}',
  androidPackage:   '${cfg.androidPackage}',
  privacyPolicyUrl: '${cfg.privacyPolicyUrl}',
  supportUrl:       '${cfg.supportUrl}',
};

export default branchConfig;
`;

fs.writeFileSync(path.join(ROOT, 'branch.config.ts'), branchConfigContent, 'utf8');

// ── Patch app.json ────────────────────────────────────────────────────────────

const appJsonPath = path.join(ROOT, 'app.json');
const appJson     = JSON.parse(fs.readFileSync(appJsonPath, 'utf8'));

appJson.expo.name                                 = cfg.appName;
appJson.expo.primaryColor                         = cfg.primaryColor;
appJson.expo.splash.backgroundColor               = cfg.backgroundColor;
appJson.expo.ios.bundleIdentifier                 = cfg.bundleId;
appJson.expo.android.package                      = cfg.androidPackage;
appJson.expo.android.adaptiveIcon.backgroundColor = cfg.backgroundColor;
appJson.expo.extra.privacyPolicyUrl               = cfg.privacyPolicyUrl;
appJson.expo.extra.supportUrl                     = cfg.supportUrl;

fs.writeFileSync(appJsonPath, JSON.stringify(appJson, null, 2) + '\n', 'utf8');

console.log(`✓  branch.config.ts   — buyer: ${cfg.branchName}`);
console.log(`✓  app.json           — bundleId: ${cfg.bundleId}`);
console.log(`\n  Run: npm run generate:assets && npm run build:prod:all`);
